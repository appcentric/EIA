          os.attribute17,
          os.attribute18,
          os.attribute19,
          os.attribute20,
          os.party_id,
          os.request_id,
          os.program_application_id,
          os.program_id,
          os.program_update_date
     FROM hz_orig_sys_references os, po_vendor_sites_all pv
