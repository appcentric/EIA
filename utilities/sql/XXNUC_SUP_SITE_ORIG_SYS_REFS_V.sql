   VENDOR_SITE_CODE_ALT,
   VENDOR_ID,
   ORG_ID,
   ORIG_SYSTEM_REF_ID,
   ORIG_SYSTEM,
   ORIG_SYSTEM_REFERENCE,
   OWNER_TABLE_NAME,
   OWNER_TABLE_ID,
